    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        Date fechaInicio = jdcDesde.getDate();
        long fi = fechaInicio.getTime();
        java.sql.Date fecInicio = new java.sql.Date(fi);

        Date fechaFin = jdcHasta.getDate();
        long ff = fechaFin.getTime();
        java.sql.Date fecFin = new java.sql.Date(ff);

        String aprobados = total(fecInicio, fecFin, "1");
        String noaprobados = total(fecInicio, fecFin, "2");

        lblAprobados.setText(aprobados);
        lblNoAprobados.setText(noaprobados);

        DefaultCategoryDataset ds = new DefaultCategoryDataset();
        ds.addValue(Integer.parseInt(aprobados), "Aprobados", "");
        ds.addValue(Integer.parseInt(noaprobados), "No Aprobados", "");

        JFreeChart jf = ChartFactory.createBarChart3D("Certificados", "Estados", "Cantidades", ds, PlotOrientation.VERTICAL, true, true, true);

//         ChartFrame f = new ChartFrame("Certificados", jf);
//         f.setSize(1000, 600);
//         f.setLocationRelativeTo(null);
//         f.setVisible(true);
        jpchart.removeAll();
        ChartPanel panel = new ChartPanel(jf);
        
        jpchart.setLayout(new java.awt.GridLayout());
        jpchart.add(panel,BorderLayout.CENTER);

jpchart.validate();
    }//GEN-LAST:event_btnConsultarActionPerformed

