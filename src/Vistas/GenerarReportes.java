    public String total(Date fecInicio, Date fecFin, String estado) {
        String total = "";
        try {
            // TODO add your handling code here:

            Conexion c = new Conexion();
            Connection con = c.getConexion();

            PreparedStatement ps = null;
            ResultSet rs = null;
            String sql1 = "SELECT COUNT(id) as total FROM certificados WHERE fecha_registro >= '" + fecInicio + "' AND fecha_registro <= '" + fecFin + "' AND estado ='" + estado + "'";

            ps = con.prepareStatement(sql1);
            rs = ps.executeQuery();
            if (rs.next()) {
                total = rs.getString(1);
            }

        } catch (SQLException ex) {
            Logger.getLogger(GenerarReportes.class.getName()).log(Level.SEVERE, null, ex);
        }
        return total;
    }

